public class Musica {
    private String titulo;
    private int duracao;


    public void setTitulo(String titulo) {
        this.titulo = titulo;
    }

    public String getTitulo(){
        return this.titulo;
    }

    public void setDuracao(int duracao){
        this.duracao = duracao;
    }

    public int getDuracao(){
        return this.duracao;
    }
}
